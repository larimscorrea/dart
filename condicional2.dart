 /* SWITCH / CASE / DEFAULT 
   * Utilizado geralmente quando temos constantes
   * Cada cláusula de case não vazio termina com uma instrução break, como regra.
   */
  switch(linguagem){
    case "Dart":
      print("É Dart!");
      break;
    case "Java":
      print("É Java!");
      break;
    case "C#":
      print("É C#!");
      break;
    default:
      print("Não sabe no que programa");
  }
